     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,['name'=>'required|unique:categories']);
        $save_category = new Category($request->all());
        $save_category->save();
        return redirect()->route('category.index');
    }

